"""ACG Sampler - Sampler that uses ACGScheduler.

This module provides ACGSampler, which executes sampling using
the schedule generated by ACGScheduler, with proper handling of
COOL/HEAT steps and Consensus operations.
"""

from __future__ import annotations

from typing import Any, Callable, Dict, List, Optional, Union

import torch

from acg.samplers.base import Sampler, SamplerOutput
from acg.schedulers import ACGScheduler, SamplingStep, StepType, compute_heat_noise_std


class ACGSampler:
    """Sampler that uses ACGScheduler for Annealed Co-Generation.

    This sampler executes the schedule generated by ACGScheduler,
    handling COOL (denoising) and HEAT (noise injection) steps,
    and applying Consensus when is_sync_step is True.

    Parameters
    ----------
    base_sampler : Sampler
        The underlying sampler for COOL steps (e.g., HeunSampler).
    scheduler : ACGScheduler
        The scheduler that controls heating and sync.

    Examples
    --------
    >>> from acg.samplers import HeunSampler, ACGSampler
    >>> from acg.schedulers import create_acg_scheduler
    >>>
    >>> base = HeunSampler()
    >>> scheduler = create_acg_scheduler(heat_jump=2, num_iterations=3)
    >>> sampler = ACGSampler(base, scheduler)
    >>>
    >>> output = sampler.sample(denoiser, latents, sigmas)
    """

    def __init__(
        self,
        base_sampler: Sampler,
        scheduler: ACGScheduler,
    ) -> None:
        self.base_sampler = base_sampler
        self.scheduler = scheduler

    def sample(
        self,
        denoiser: Callable[..., torch.Tensor],
        latents: torch.Tensor,
        sigmas: torch.Tensor,
        return_trajectory: bool = False,
        step_callback: Optional[Callable[[SamplingStep, torch.Tensor], None]] = None,
        **kwargs: Any,
    ) -> SamplerOutput:
        """Run sampling with ACG schedule.

        Parameters
        ----------
        denoiser : Callable
            Denoiser function D(x, sigma, ...).
        latents : torch.Tensor
            Initial noise of shape [B, ...].
        sigmas : torch.Tensor
            Noise schedule of shape [n_steps + 1].
        return_trajectory : bool, optional
            If True, record all intermediate states.
        step_callback : Callable, optional
            Called after each step with (step, x).
        **kwargs
            Additional arguments passed to denoiser.

        Returns
        -------
        SamplerOutput
            Sampling result with samples and optional trajectory.
        """
        # Get schedule
        sigmas_np = sigmas.cpu().numpy()
        schedule = self.scheduler.get_schedule(sigmas_np)

        x = latents
        trajectory: List[torch.Tensor] = []
        nfe = 0

        if return_trajectory:
            trajectory.append(x.clone())

        for step in schedule:
            if step.step_type == StepType.COOL:
                # Denoising step
                sigma = torch.tensor(step.sigma_from, device=x.device, dtype=x.dtype)
                sigma_next = torch.tensor(step.sigma_to, device=x.device, dtype=x.dtype)

                x = self.base_sampler.step(
                    denoiser=denoiser,
                    x=x,
                    sigma=sigma,
                    sigma_next=sigma_next,
                    **kwargs,
                )

                # Count function evaluations
                # Heun uses 2 evals except for last step
                if step.sigma_to > 0:
                    nfe += 2
                else:
                    nfe += 1

            else:  # StepType.HEAT
                # Forward diffusion: add noise
                noise_std = compute_heat_noise_std(step.sigma_from, step.sigma_to)
                x = x + noise_std * torch.randn_like(x)

            if return_trajectory:
                trajectory.append(x.clone())

            if step_callback is not None:
                step_callback(step, x)

        if return_trajectory:
            return SamplerOutput(
                samples=x,
                trajectories=torch.stack(trajectory, dim=1),
                sigmas=sigmas,
                num_function_evaluations=nfe,
            )

        return SamplerOutput(
            samples=x,
            sigmas=sigmas,
            num_function_evaluations=nfe,
        )

    def sample_multi_context(
        self,
        denoiser: Callable[..., torch.Tensor],
        latents_dict: Dict[str, torch.Tensor],
        sigmas: torch.Tensor,
        consensus_fn: Optional[Callable[[Dict[str, torch.Tensor]], torch.Tensor]] = None,
        shared_keys: Optional[List[str]] = None,
        return_trajectory: bool = False,
        step_callback: Optional[Callable[[SamplingStep, Dict[str, torch.Tensor]], None]] = None,
        **kwargs: Any,
    ) -> Dict[str, SamplerOutput]:
        """Run sampling with multiple contexts and Consensus.

        This method implements the full ACG algorithm where multiple
        variables evolve together and shared variables are synchronized
        using the Consensus operator.

        Parameters
        ----------
        denoiser : Callable
            Denoiser function. Can be context-aware.
        latents_dict : Dict[str, torch.Tensor]
            Initial latents for each variable.
            Example: {'A': A_T, 'C': C_T, 'B_A': B_T^(A), 'B_C': B_T^(C)}
        sigmas : torch.Tensor
            Noise schedule.
        consensus_fn : Callable, optional
            Function to compute consensus from shared variables.
            Input: dict of shared variable tensors.
            Output: consensus tensor.
            Default: simple mean.
        shared_keys : List[str], optional
            Keys of variables to synchronize (e.g., ['B_A', 'B_C', 'B_uncond']).
            If None, no consensus is applied.
        return_trajectory : bool, optional
            If True, record trajectories for all variables.
        step_callback : Callable, optional
            Called after each step.
        **kwargs
            Additional arguments passed to denoiser.

        Returns
        -------
        Dict[str, SamplerOutput]
            Sampling results for each variable.

        Examples
        --------
        >>> latents = {
        ...     'A': torch.randn(B, D) * sigma_max,
        ...     'C': torch.randn(B, D) * sigma_max,
        ...     'B_A': torch.randn(B, D) * sigma_max,
        ...     'B_C': torch.randn(B, D) * sigma_max,
        ... }
        >>> results = sampler.sample_multi_context(
        ...     denoiser, latents, sigmas,
        ...     shared_keys=['B_A', 'B_C'],
        ...     consensus_fn=lambda d: sum(d.values()) / len(d),
        ... )
        """
        # Default consensus: mean
        if consensus_fn is None:
            def consensus_fn(d: Dict[str, torch.Tensor]) -> torch.Tensor:
                values = list(d.values())
                return sum(values) / len(values)

        if shared_keys is None:
            shared_keys = []

        # Get schedule
        sigmas_np = sigmas.cpu().numpy()
        schedule = self.scheduler.get_schedule(sigmas_np)

        # Initialize state
        x_dict = {k: v.clone() for k, v in latents_dict.items()}
        trajectories: Dict[str, List[torch.Tensor]] = {k: [] for k in x_dict}
        nfe_dict: Dict[str, int] = {k: 0 for k in x_dict}

        if return_trajectory:
            for k in x_dict:
                trajectories[k].append(x_dict[k].clone())

        # Get device from first tensor
        device = next(iter(x_dict.values())).device
        dtype = next(iter(x_dict.values())).dtype

        for step in schedule:
            # Phase 1: Consensus (if sync step and we have shared keys)
            if step.is_sync_step and shared_keys:
                shared_dict = {k: x_dict[k] for k in shared_keys}
                consensus = consensus_fn(shared_dict)
                for k in shared_keys:
                    x_dict[k] = consensus.clone()

            if step.step_type == StepType.COOL:
                # Phase 3: Cooling (denoising)
                sigma = torch.tensor(step.sigma_from, device=device, dtype=dtype)
                sigma_next = torch.tensor(step.sigma_to, device=device, dtype=dtype)

                for k in x_dict:
                    x_dict[k] = self.base_sampler.step(
                        denoiser=denoiser,
                        x=x_dict[k],
                        sigma=sigma,
                        sigma_next=sigma_next,
                        **kwargs,
                    )

                    if step.sigma_to > 0:
                        nfe_dict[k] += 2
                    else:
                        nfe_dict[k] += 1

            else:  # StepType.HEAT
                # Phase 2: Heating (noise injection)
                noise_std = compute_heat_noise_std(step.sigma_from, step.sigma_to)
                for k in x_dict:
                    x_dict[k] = x_dict[k] + noise_std * torch.randn_like(x_dict[k])

            if return_trajectory:
                for k in x_dict:
                    trajectories[k].append(x_dict[k].clone())

            if step_callback is not None:
                step_callback(step, x_dict)

        # Build outputs
        results = {}
        for k in x_dict:
            if return_trajectory:
                results[k] = SamplerOutput(
                    samples=x_dict[k],
                    trajectories=torch.stack(trajectories[k], dim=1),
                    sigmas=sigmas,
                    num_function_evaluations=nfe_dict[k],
                )
            else:
                results[k] = SamplerOutput(
                    samples=x_dict[k],
                    sigmas=sigmas,
                    num_function_evaluations=nfe_dict[k],
                )

        return results

    def __repr__(self) -> str:
        return f"ACGSampler(base_sampler={self.base_sampler}, scheduler={self.scheduler})"
